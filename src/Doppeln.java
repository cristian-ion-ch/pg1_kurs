import java.util.Scanner;
public class Doppeln {
    static void main(String[] args) {
        Scanner eingabe = new Scanner(System.in);
        int zahl;
        System.out.println("Geben Sie eine Zahl ein!");
        zahl = eingabe.nextInt();
        zahl = 5;
        zahl = zahl * 2;
        System.out.println(zahl);
    }
}
